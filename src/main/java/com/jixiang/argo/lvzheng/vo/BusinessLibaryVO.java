public class BusinessLibaryVO {
	private String companymc;
	private long addperson;
	private String username;
	private String userphone;
	private String busid;
	public String getCompanymc() {
		return companymc;
	}
	public void setCompanymc(String companymc) {
		this.companymc = companymc;
	}
	public long getAddperson() {
		return addperson;
	}
	public void setAddperson(long addperson) {
		this.addperson = addperson;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getUserphone() {
		return userphone;
	}
	public void setUserphone(String userphone) {
		this.userphone = userphone;
	}
	public String getBusid() {
		return busid;
	}
	public void setBusid(String busid) {
		this.busid = busid;
	}
	
}
